public class VarTest09 {
    public static void main(String[] args) {
        int a = 0b0111;
        int b = 111;
        int c = 026;
        int d = 0x4f;
        System.out.println("a = " + a); //7
        System.out.println("b = " + b); //111
        System.out.println("c = " + c); //22
        System.out.println("d = " + d); //79
    }
}
